    protected void datatable() {
    Object[] Baris = {"ID Kasir", "Nama Kasir", "Jenis Kelamin", "Telepon", "Agama", "Alamat", "Password"};
    // BARIS DI BAWAH INI ADALAH KUNCI: Menghapus tampilan lama sebelum refresh
    tabmode = new DefaultTableModel(null, Baris); 
    
    String cariitem = txtcari.getText();
    try {
        String sql = "SELECT * FROM kasir WHERE id_kasir  LIKE '%"+cariitem+"%' OR nm_kasir LIKE '%"+cariitem+"%' ORDER BY id_kasir ASC";;
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while (hasil.next()) {
            tabmode.addRow(new Object[]{
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
                hasil.getString(6),
                hasil.getString(7)
            });
        }
        tblkasir.setModel(tabmode);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal dipanggil: " + e);
    }
    }

